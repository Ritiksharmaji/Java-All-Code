package javaOops._1classesAndObject;

public class Class1 {
	  // data member (also instance variable)
    	int id;
    // data member (also instance variable)
    	String n;
	public static void main(String[] args) {
		// creating an object of Class1
    
		Class1 s1 = new Class1();
        System.out.println(s1.id);
        System.out.println(s1.n);
	}

}
